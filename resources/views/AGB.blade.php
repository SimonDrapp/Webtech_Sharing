            verpflichtet, den Anbieter zunächst unverzüglich telefonisch über Schadensereignisse zu informieren und
            den
            Anbieter nachfolgend über alle Einzelheiten mittels Textform in allen Punkten vollständig und sorgfältig
            zu
            unterrichten. Ereignet sich der Schaden im Inland, ohne dass der KUNDE bzw. die fahrtberechtigte Person
            hierbei
            verletzt wurden, hat die Unterrichtung spätestens sieben Tage nach dem Schadensereignis, ansonsten
            innerhalb von
            14 Tagen nach dem Schadensereignis zu erfolgen. Geht innerhalb dieser Frist keine Schadensmeldung beim
            Anbieter
            ein, so kann der Anbieter die daraus entstehenden Mehraufwände dem KUNDEN in Rechnung stellen. Kann ein
            Unfall
            nicht von der Versicherung reguliert werden, weil der KUNDE die Auskunft verweigert, so behält sich der
            Anbieter
            vor, dem KUNDEN alle unfallbedingten Kosten für Schäden an Personen, Gegenständen und Fahrzeugen zu
            belasten.
