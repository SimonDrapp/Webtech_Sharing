            Werktages Textform anzuzeigen.</p>
        <p class="unterteil"><b>§ 12.2 Auslandsfahrten</b></p>
        <p>Die Benutzung ist nur innerhalb Europas gestattet. Auslandsfahrten sind vor Fahrtantritt dem Anbieter in
