            für die Zulassung und Benutzung von Fahrzeugen in Deutschland gelten, Verkehrsregeln sowie Anforderungen
            an die
            Fahrerlaubnis, trägt ausschließlich der KUNDE die Verantwortung und stellt hierbei den Anbieter von
            jeglichen
            Ansprüchen frei.</p>
        <p class="unterteil"><b>§ 12.3 Nutzungsdauer und Rückgabe</b></p>
        <p>Die Nutzungsdauer umfasst den vereinbarten Mietzeitraum, das Fahrzeug wird persönlich übergeben und auch
            persönlich zurückgenommen. Die Rückgabe gilt als ordnungsgemäß, wenn das Fahrzeug mit allen übergebenen
            Dokumenten und Ausstattungsgegenständen an den Entleiher oder dessen Beauftragten zurückgegeben worden
            ist. Bei
            Übernahme und Rückgabe ist ein Zustandsprotokoll anzufertigen.</p>
        <p class="unterteil"><b>§ 12.4 Stornierungen</b></p>
        <p>Kann ein KUNDE das gebuchte Fahrzeug nicht nutzen, kann eine Stornierung erfolgen. Die Stornierung einer
            Buchung
            kann mit einer Gebühr gemäß Preis- und Gebührenliste belegt werden. Verkürzungen von Buchungen werden
            wie
            Stornierungen des verkürzten Zeitraumes behandelt. Der Anbieter informiert den KUNDEN, wenn die gebuchte
            Fahrzeugklasse nicht zur Verfügung gestellt werden kann. Der KUNDE kann dann die Buchung kostenfrei
            stornieren
            oder im Rahmen der Verfügbarkeit auf eine andere Fahrzeugklasse umbuchen.</p>
        <p class="unterteil"><b>§ 12.5 Verspätungen</b></p>
        <p>Kann der KUNDE den vereinbarten Rückgabezeitpunkt nicht einhalten, muss er die Buchungsdauer vor Ablauf
            des
            zunächst vereinbarten Rückgabezeitpunktes verlängern. Ist eine Verlängerung wegen einer nachfolgenden
            Buchung
            nicht möglich und kann die ursprüngliche Rückgabezeit tatsächlich durch den KUNDEN nicht eingehalten
            werden, ist
