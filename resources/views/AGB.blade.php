            teilweise oder gänzlich verschuldeten Unfall eine Aufwandspauschale gemäß aktueller Gebührenliste
            berechnen,
            soweit der KUNDE dem Anbieter nicht nachweist, dass diesem kein oder nur ein wesentlich geringerer
            Schaden
            entstanden ist. Der KUNDE darf sich nach einem Unfall (unabhängig von dessen Verschuldung) erst vom
