            entfernen, wenn die polizeiliche Aufnahme abgeschlossen ist und die Sicherstellung des Fahrzeuges nach
            Rücksprache mit dem Kundenservice gewährleistet werden konnte.</p>
        <h7><b>§ 14 Versicherung</b></h7>
        <p>Für alle Fahrzeuge besteht eine Haftpflicht-, Teilkasko- und Vollkaskoversicherung. Die jeweiligen
            Selbstbeteiligungen und die Möglichkeit der Inanspruchnahme eines weiteren Versicherungsschutzes durch
            den
            KUNDEN ergeben sich aus der gültigen Preisliste. Die Senkung der Selbstbeteiligung im Schadensfall gilt
            nur,
            wenn dieser vor Fahrtantritt gebucht wurde und die fahrtberechtigte Person bei der Buchung angegeben
            wurde,
            sofern der KUNDE nicht selbst Fahrer ist. Die Inanspruchnahme von Versicherungsleistungen ist nur nach
            vorheriger Zustimmung des Anbieters zulässig.</p>
        <h7><b>§ 15 Technikereinsatz</b></h7>
        <p>Verursacht der KUNDE einen Technikereinsatz durch nicht sachgemäße Bedienung des Fahrzeugs bzw. der
            Zugangstechnik, durch Nichteinhalten der ordnungsgemäßen Bedienung des Fahrzeugs oder der Regeln
